    private void loadSubjects() {

        try {

            ResultSet rs = MySQL.executeSearch("SELECT * FROM `subject`");

            Vector<String> vector = new Vector<>();
            vector.add("Select");

            while (rs.next()) {

                vector.add(rs.getString("name"));

                subjectMap.put(rs.getString("name"), rs.getString("no"));

            }

            DefaultComboBoxModel model = new DefaultComboBoxModel(vector);
            jComboBox1.setModel(model);

        } catch (Exception e) {
            e.printStackTrace();
        }

    }
    
    private void loadTeachersSubjects() {

        try {

            ResultSet rs = MySQL.executeSearch("SELECT DISTINCT `class`.`subject_no`, `subject`.`name`, `subject`.`no` FROM `class` INNER JOIN `subject` ON `subject`.`no`=`class`.`subject_no` WHERE `teacher_no`='"+tId+"'");

            Vector<String> vector = new Vector<>();
            vector.add("Select");

            while (rs.next()) {

                vector.add(rs.getString("subject.name"));

                tsubjectMap.put(rs.getString("subject.name"), rs.getString("subject.no"));

            }

            DefaultComboBoxModel model = new DefaultComboBoxModel(vector);
            jComboBox2.setModel(model);

        } catch (Exception e) {
            e.printStackTrace();
        }

    }
    
    private void loadTable(){
        
        
        try {
            
            String query = "SELECT * FROM `class` INNER JOIN `subject` ON `subject`.`no`=`class`.`subject_no` INNER JOIN `teacher` ON `teacher`.`no`=`class`.`teacher_no` WHERE `teacher`.`no`='"+tId+"'";
            
            String selectedSubject = String.valueOf(jComboBox2.getSelectedItem());
            
            if (selectedSubject.equals(tsubjectMap.get("name"))) {
                query += "AND `class`.`subject_no`='"+tsubjectMap.get("no")+"'";
            }
            
            ResultSet rs = MySQL.executeSearch(query);
            
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (rs.next()) {

                Vector<String> vector = new Vector<>();
                vector.add(rs.getString("class.name"));
                vector.add(rs.getString("timeslot"));
//                vector.add(rs.getString("mobile"));
//                vector.add(rs.getString("address"));
//                vector.add(rs.getString("subject.name"));

                model.addRow(vector);

            }
            
            
        } catch (Exception e) {
            e.printStackTrace();
        }
        
    }

