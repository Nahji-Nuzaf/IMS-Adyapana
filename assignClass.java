    private void roundButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_roundButton6ActionPerformed

        String timeslot = roundTextField1.getText();
        String className = roundTextField2.getText();
        String tsub = String.valueOf(jComboBox1.getSelectedItem());

        if (tsub.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please Select a Subject", "Warning", JOptionPane.WARNING_MESSAGE);
            
        } else if (className.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter the Class Name", "Warning", JOptionPane.WARNING_MESSAGE);
            roundTextField2.grabFocus();
        } else if (timeslot.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter the Timeslot", "Warning", JOptionPane.WARNING_MESSAGE);
            roundTextField1.grabFocus();
        }else{

            try {


                
                ResultSet rs = MySQL.executeSearch("SELECT * FROM `class` INNER JOIN `teacher` ON `teacher`.`no`=`class`.`teacher_no` "
                        + "INNER JOIN `subject` ON `subject`.`no`=`class`.`subject_no` "
                        + "WHERE `teacher_no`='" + tId + "' AND `class`.`timeslot`='" + timeslot + "' ");

                if (rs.next()) {

                    JOptionPane.showMessageDialog(this, "Time Already Registered", "Warning", JOptionPane.WARNING_MESSAGE);

                } else {

//                    MySQL.executeIUD("UPDATE `teacher` SET `subject_no`='"+subjectMap.get(tsub)+"' WHERE `teacher`.`no`='"+tId+"'");
                    
//                    String thsId = rs.getString("teacher_has_subject.id");
                    MySQL.executeIUD("INSERT INTO `class` (`timeslot`,`subject_no`,`teacher_no`,`name`) VALUES ('" + timeslot + "','" + subjectMap.get(tsub) + "','"+tId+"','"+className+"')");
                    
                    

                    this.dispose();

                }

            } catch (Exception e) {
                e.printStackTrace();
            }

        }


    }//GEN-LAST:event_roundButton6ActionPerformed

